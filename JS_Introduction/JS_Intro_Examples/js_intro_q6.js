/*
Write a program to convert kilometers to miles and miles to kilometers
*/

//miles to kilometers
//miles = kilometers * 0.621371

const kilometersA = 25;

//calculate miles
const milesA = kilometersA * 0.621371;

//display the miles 
console.log(`${kilometersA} kilometers is ${milesA} miles`);

//kiiometers to miles
//kilometer = miles * 1.60934
const milesB = 25;

//calculate kilometers
const kilometersB = milesB * 1.60934;

//display kilometers
console.log(`${milesB} miles is ${kilometersB} kilometers`);